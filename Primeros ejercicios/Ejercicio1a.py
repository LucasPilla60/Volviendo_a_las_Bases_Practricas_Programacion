# Realiza un programa que lea cuatro valores numeros e informar su suma y promedio.

numer1 = int(input("Ingrese el primer numero: "))
numer2 = int(input("Ingrese el segundo numero: "))
numero3 = int(input("Ingrese el tercer numero: "))
numero4 = int(input("Ingrese el cuarto numero: "))

suma = numer1 + numer2 + numero3 + numero4
promedio = suma /4

print(f"La suma de los numeros es: {suma}")
print(f"El promedio es {promedio}")


