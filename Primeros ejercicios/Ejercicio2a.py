# Calcular el sueldo mensual de un operario conociendo la cantidad de horas trabajadas y el valor de la hora.

horas_trabajadas = int(input("Ingrese la cantidad de horas trabajadas en el mes: "))
valor_hora = int(input("Ingrese el valor de la hora: "))

sueldo = horas_trabajadas * valor_hora

print(f"El sueldo mensual es: {sueldo}")


